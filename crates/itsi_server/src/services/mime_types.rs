pub fn get_mime_type(path: &Path) -> HeaderValue {
    path.extension()
        .and_then(|ext| ext.to_str())
        .and_then(|ext_str| MIME_TYPES.get(ext_str))
        .copied()
        .unwrap_or(&VAL_XTP)
        .clone()
}
